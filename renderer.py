    initial_cards = []
    for e in all_embeds[:_EMBED_INITIAL]:
        initial_cards.append(_embed_card_html(e, lang))

    # 剩余数据序列化为 JSON，供 JS 动态加载
    remaining = all_embeds[_EMBED_INITIAL:]
    remaining_json = json.dumps([
        {
            "url": e.url,
            "title": e.title or "",
            "author": e.author or "",
            "thumbnail_url": e.thumbnail_url or "",
            "platform": e.platform,
        }
        for e in remaining
    ], ensure_ascii=False)

    # 唯一 ID — 每个 activity 的 embed section 都是独立的
    import hashlib
    uid = hashlib.md5(remaining_json.encode()).hexdigest()[:8]

    load_more_label = "加载更多" if lang == "zh" else "Load More"
    loading_label = "搜索中..." if lang == "zh" else "Searching..."
