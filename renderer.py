# ── 地图相关 ──────────────────────────────────────────────────────────────────

def _collect_map_data(
    plan: TravelPlan, places: dict[str, PlaceInfo]
) -> list[dict]:
    """从计划中提取所有有坐标的地点，用于地图渲染。"""
    points = []
    for day in plan.daily_plans:
        for idx, act in enumerate(day.activities):
            info = places.get(act.place_name)
            if info and info.lat is not None and info.lon is not None:
                points.append({
                    "name": act.place_name,
                    "lat": info.lat,
                    "lon": info.lon,
                    "day": day.day,
                    "index": idx + 1,
                    "time": act.time_slot,
                    "isRestaurant": getattr(act, "is_restaurant", False),
                })
    return points


