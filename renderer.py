_SLOT_KEYS = ["morning", "afternoon", "evening"]
_SLOT_LABEL_KEYS = {
    "morning": "slot_morning",
    "afternoon": "slot_afternoon",
    "evening": "slot_evening",
}

# Legacy string-based fallback (for old plans)
_SLOT_NORM_LEGACY: dict[str, str] = {
    "上午": "morning", "下午": "afternoon", "晚上": "evening",
    "Morning": "morning", "Afternoon": "afternoon", "Evening": "evening",
    "morning": "morning", "afternoon": "afternoon", "evening": "evening",
    "中午": "afternoon", "午餐": "afternoon", "Lunch": "afternoon",
    "晚餐": "evening", "Dinner": "evening", "dinner": "evening",
    "夜间": "evening", "night": "evening", "Night": "evening",
}


def _parse_time_slot(raw: str) -> tuple[str, int | None]:
    """Parse time_slot → (slot_key, hour).
    Accepts 'HH:MM' format or legacy labels like '上午'/'Morning'.
    Returns (morning/afternoon/evening, hour_int_or_None).
    """
    import re
    m = re.match(r'^(\d{1,2}):(\d{2})$', raw.strip())
    if m:
        hour = int(m.group(1))
        if hour < 12:
            return "morning", hour
        elif hour < 17:
            return "afternoon", hour
        else:
            return "evening", hour
    # Legacy fallback
    return _SLOT_NORM_LEGACY.get(raw.strip(), "evening"), None


def _format_time_range(start: str, duration_minutes: int) -> str:
    """'09:00' + 90min → '09:00 – 10:30'."""
    import re
    m = re.match(r'^(\d{1,2}):(\d{2})$', start.strip())
    if not m:
        return ""
    h, mi = int(m.group(1)), int(m.group(2))
    end_total = h * 60 + mi + duration_minutes
    eh, em = divmod(end_total, 60)
    if eh >= 24:
        eh = eh % 24
    return f"{h:02d}:{mi:02d} – {eh:02d}:{em:02d}"


