def save_and_open(html: str, filename: str = "travel_plan.html") -> Path:
    """保存 HTML 并在浏览器中打开。"""
    path = Path(filename).resolve()
    path.write_text(html, encoding="utf-8")
    webbrowser.open(f"file://{path}")
    return path
