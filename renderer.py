def _render_day(
    day,
    places: dict[str, PlaceInfo],
    links: dict[str, RecommendationLinks] | None = None,
    embeds: dict[str, PlaceEmbeds] | None = None,
    has_map: bool = False,
    lang: str = "zh",
    destination: str = "",
    api_hotel=None,
    transits: list | None = None,
) -> str:
    L = LABELS[lang]
    day_map_html = _render_day_map_div(day.day) if has_map else ""

    # 建立 activity index → transit 的映射（跨时间段的全局 index）
    all_acts = day.activities
    transit_list = transits or []

    # 预算每对相邻活动之间的空闲时间 (global index → free minutes)
    free_after: dict[int, int] = {}
    for i in range(len(all_acts) - 1):
        gap = _calc_free_time_minutes(all_acts[i], all_acts[i + 1])
        if gap > 0:
            free_after[i] = gap

    # 按时间段分组，同时记录每个 act 在全局列表中的 index
    from collections import defaultdict
    grouped: dict[str, list[tuple[int, object]]] = defaultdict(list)
    for gi, act in enumerate(all_acts):
        slot_key, _ = _parse_time_slot(act.time_slot)
        grouped[slot_key].append((gi, act))

    zones_html = ""
    for slot_key in _SLOT_KEYS:
        items = grouped[slot_key]
        inner = ""
        for li, (gi, act) in enumerate(items):
            place_info = places.get(act.place_name)
            act_links = links.get(act.place_name) if links else None
            act_embeds = embeds.get(act.place_name) if embeds else None
            inner += _render_activity(act, place_info, act_links, act_embeds, lang)
            # 插入通行时间（当前活动 → 下一个活动）
            if gi < len(transit_list) and transit_list[gi] is not None:
                inner += _render_transit_row(transit_list[gi], lang)
            # 插入空闲时间
            if gi in free_after:
                inner += _render_free_time_row(free_after[gi], lang)

        plus_icon = '<svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><line x1="12" y1="5" x2="12" y2="19"/><line x1="5" y1="12" x2="19" y2="12"/></svg>'
        if not inner:
            inner = f'<div class="slot-empty">{plus_icon}{L["slot_empty"]}</div>'
            tail = ""
        else:
            drop_label = "拖放到此处" if lang == "zh" else "Drop here"
            tail = f'<div class="drop-tail">{plus_icon} {drop_label}</div>'

        slot_label = L[_SLOT_LABEL_KEYS[slot_key]]
        zones_html += f"""
      <div class="time-slot-zone" data-slot="{slot_key}">
        <div class="slot-header">{slot_label}</div>
        <div class="activities-zone" data-slot="{slot_key}">
          {inner}
          {tail}
        </div>
      </div>"""

    hotel_html = _render_day_hotel(getattr(day, "hotel", None), api_hotel, destination, lang)

    return f"""
    <div class="day-card" data-day="{day.day}">
      <div class="day-header">
        <h2>Day {day.day}</h2>
        <span>{html_lib.escape(day.theme)}</span>
      </div>
      {day_map_html}
      {zones_html}
      {hotel_html}
    </div>"""


def _markdown_table_to_html(md: str) -> str:
    lines = [l.strip() for l in md.strip().split("\n") if l.strip()]
    if not lines:
        return f"<p>{html_lib.escape(md)}</p>"
    if "|" not in lines[0]:
        return f"<p>{html_lib.escape(md)}</p>"

    rows = []
    for i, line in enumerate(lines):
        cells = [c.strip() for c in line.split("|") if c.strip()]
        if not cells:
            continue
        if all(set(c) <= set("-: ") for c in cells):
            continue
        tag = "th" if i == 0 else "td"
        row = "".join(f"<{tag}>{html_lib.escape(c)}</{tag}>" for c in cells)
        rows.append(f"<tr>{row}</tr>")

    return f"<table>{''.join(rows)}</table>"


